const mongoose = require('mongoose');
const bcrypt = require('bcryptjs');
require('dotenv').config();

// Import User model
const User = require('./models/User');

const resetPasswords = async () => {
  try {
    console.log('ğŸ”Œ Connecting to MongoDB...');
    
    const mongoUri = process.env.MONGODB_URI;
    if (!mongoUri) {
      console.error('âŒ MongoDB URI is not defined.');
      process.exit(1);
    }

    await mongoose.connect(mongoUri, {
      useNewUrlParser: true,
      useUnifiedTopology: true,
    });

    console.log('âœ… Connected to MongoDB');

    // Define the original passwords from seeder
    const passwordMap = {
      'superadmin': 'admin123',
      'kigali_manager': 'kigali123',
      'kampala_manager': 'kampala123',
      'nairobi_manager': 'nairobi123',
      'juba_manager': 'juba123',
      'kampala_nairobi_route': 'route123',
      'goma_kampala_route': 'route123',
      'nairobi_kigali_route': 'route123',
      'kampala_kigali_route': 'route123',
      'kampala_juba_route': 'route123',
      'juba_bor_route': 'route123',
      'active_fleet_manager': 'fleet123',
      'maintenance_fleet_manager': 'fleet123'
    };

    console.log('ğŸ”„ Resetting passwords...');

    for (const [username, password] of Object.entries(passwordMap)) {
      const hashedPassword = await bcrypt.hash(password, 12);
      
      const result = await User.updateOne(
        { username: username },
        {
          $set: {
            password: hashedPassword,
            isPasswordChanged: false,
            autoGeneratedPassword: undefined,
            loginAttempts: 0,
            lockUntil: null
          }
        }
      );

      if (result.modifiedCount > 0) {
        console.log(`âœ… ${username}: ${password}`);
      } else {
        console.log(`âŒ ${username}: User not found`);
      }
    }

    console.log('\nğŸ‰ All passwords have been reset!');
    console.log('\nğŸ” LOGIN CREDENTIALS:');
    console.log('='.repeat(50));
    
    for (const [username, password] of Object.entries(passwordMap)) {
      console.log(`ğŸ‘¤ ${username} / ${password}`);
    }

    console.log('='.repeat(50));
    console.log('ğŸ’¡ Try logging in with any of these credentials now!');

    process.exit(0);
  } catch (error) {
    console.error('âŒ Error resetting passwords:', error);
    process.exit(1);
  }
};

resetPasswords();
